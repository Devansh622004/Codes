//  CASE 1: Iterating over an Array

let numbers = [10, 20, 30];

for (let value of numbers) {
  console.log(value);
}


// CASE 2:  Iterating over a String

let str = "hello";

for (let char of str) {
  console.log(char);
}


// CASE 3: Iterating over a Set

let uniqueNumbers = new Set([1, 2, 3]);

for (let value of uniqueNumbers) {
  console.log(value);
}

